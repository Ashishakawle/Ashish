package casting;

public class Implicit {

	public static void main(String[] args) {
		
		//Lower datatype is converted into higher data type
		int i=5;
		System.out.println(i);
		
		double d=i;
		System.out.println(d);
		
		float f=10.5f;
		System.out.println(f);
		
		double m=f;
		System.out.println(m);
		
	}
	
}
